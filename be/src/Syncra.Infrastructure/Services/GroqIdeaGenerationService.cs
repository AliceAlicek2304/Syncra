    public async Task<GenerateIdeasResponseDto> GenerateIdeasAsync(
        Guid workspaceId,
        Guid userId,
        GenerateIdeasRequestDto request,
        CancellationToken cancellationToken = default)
    {
        if (string.IsNullOrWhiteSpace(request.Topic))
        {
            throw new ArgumentException("Topic is required.", nameof(request));
        }

        if (string.IsNullOrWhiteSpace(_options.ApiKey))
        {
            throw new InvalidOperationException("Groq API key is not configured. Set Groq__ApiKey in your environment.");
        }

        var count = Math.Clamp(request.Count <= 0 ? 3 : request.Count, 1, 10);

        var completion = await SendGroqJsonAsync(
            BuildIdeaSystemPrompt(),
            BuildIdeaUserPrompt(request, count),
            cancellationToken);

        var content = completion?.Choices?.FirstOrDefault()?.Message?.Content;
        if (string.IsNullOrWhiteSpace(content))
        {
            throw new InvalidOperationException("Groq returned empty content.");
        }

        var json = ExtractJson(content);
        var sanitizedJson = SanitizeJson(json);
        var parsed = JsonSerializer.Deserialize<IdeasEnvelope>(sanitizedJson, JsonOptions);
        var normalized = NormalizeIdeas(parsed?.Ideas, count);

        if (normalized.Count == 0)
        {
            throw new InvalidOperationException("No valid ideas generated by Groq.");
        }

        return new GenerateIdeasResponseDto(request.Topic.Trim(), normalized);
    }

    public async Task<GenerateRepurposeResponseDto> GenerateRepurposeAsync(
        Guid workspaceId,
        Guid userId,
        GenerateRepurposeRequestDto request,
        CancellationToken cancellationToken = default)
    {
        if (string.IsNullOrWhiteSpace(request.SourceText))
        {
            throw new ArgumentException("SourceText is required.", nameof(request));
        }

        if (request.Platforms is null || request.Platforms.Count == 0)
        {
            throw new ArgumentException("At least one target platform is required.", nameof(request));
        }

        if (string.IsNullOrWhiteSpace(_options.ApiKey))
        {
            throw new InvalidOperationException("Groq API key is not configured. Set Groq__ApiKey in your environment.");
        }

        var completion = await SendGroqJsonAsync(
            BuildRepurposeSystemPrompt(),
            BuildRepurposeUserPrompt(request),
            cancellationToken);

        var content = completion?.Choices?.FirstOrDefault()?.Message?.Content;
        if (string.IsNullOrWhiteSpace(content))
        {
            throw new InvalidOperationException("Groq returned empty content for repurpose.");
        }

        var json = ExtractJson(content);
        var sanitizedJson = SanitizeJson(json);
        var parsed = JsonSerializer.Deserialize<RepurposeEnvelope>(sanitizedJson, JsonOptions);
        var atoms = NormalizeRepurposeAtoms(parsed?.Atoms, request.Platforms, request.ExtractAtoms);

        if (atoms.Count == 0)
        {
            throw new InvalidOperationException("No valid repurpose outputs were generated by Groq.");
        }

        return new GenerateRepurposeResponseDto(atoms);
    }

    public async Task<AssistContentResponseDto> AssistContentAsync(
        Guid workspaceId,
        Guid userId,
        AssistContentRequestDto request,
        CancellationToken cancellationToken = default)
    {
        if (string.IsNullOrWhiteSpace(request.Content))
        {
            throw new ArgumentException("Content is required.", nameof(request));
        }

        if (string.IsNullOrWhiteSpace(_options.ApiKey))
        {
            throw new InvalidOperationException("Groq API key is not configured.");
        }

        var completion = await SendGroqRawAsync(
            BuildAssistSystemPrompt(),
            BuildAssistUserPrompt(request),
            cancellationToken);

        var content = completion?.Choices?.FirstOrDefault()?.Message?.Content;
        if (string.IsNullOrWhiteSpace(content))
        {
            throw new InvalidOperationException("Groq returned empty content for assist.");
        }

        return new AssistContentResponseDto { Result = content.Trim() };
    }

    private async Task<GroqChatCompletionResponse?> SendGroqJsonAsync(
        string systemPrompt,
        string userPrompt,
        CancellationToken cancellationToken)
    {
        var candidateModels = new[] { _options.Model }
            .Concat(_options.FallbackModels ?? [])
            .Where(m => !string.IsNullOrWhiteSpace(m))
            .Select(m => m.Trim())
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .ToList();

        if (candidateModels.Count == 0)
        {
            throw new InvalidOperationException("No Groq model configured.");
        }

        Exception? lastException = null;

        foreach (var model in candidateModels)
        {
            var payload = new
            {
                model,
                temperature = _options.Temperature,
                max_completion_tokens = _options.MaxCompletionTokens,
                messages = new object[]
                {
                    new
                    {
                        role = "system",
                        content = systemPrompt
                    },
                    new
                    {
                        role = "user",
                        content = userPrompt
                    }
                }
            };

            var req = new HttpRequestMessage(HttpMethod.Post, "/openai/v1/chat/completions")
            {
                Content = new StringContent(JsonSerializer.Serialize(payload), Encoding.UTF8, "application/json")
            };
            req.Headers.Authorization = new AuthenticationHeaderValue("Bearer", _options.ApiKey);

            using var res = await _httpClient.SendAsync(req, cancellationToken);
            var body = await res.Content.ReadAsStringAsync(cancellationToken);

            if (res.IsSuccessStatusCode)
            {
                _logger.LogInformation("Groq request succeeded with model {Model}", model);
                return JsonSerializer.Deserialize<GroqChatCompletionResponse>(body, JsonOptions);
            }

            lastException = new HttpRequestException($"Groq API error ({model}): {(int)res.StatusCode}");
            _logger.LogWarning(
                "Groq API failed for model {Model} with status {StatusCode}: {Body}",
                model,
                (int)res.StatusCode,
                body);
        }

        throw lastException ?? new HttpRequestException("Groq API error: all candidate models failed.");
    }

    private async Task<GroqChatCompletionResponse?> SendGroqRawAsync(
        string systemPrompt,
        string userPrompt,
        CancellationToken cancellationToken)
    {
        // Similar to SendGroqJsonAsync but without forcing JSON mode if the model supports it, 
        // though Groq handles standard chat just fine.
        return await SendGroqJsonAsync(systemPrompt, userPrompt, cancellationToken);
    }

