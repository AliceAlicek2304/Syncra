    private static string ExtractJson(string raw)
    {
        var trimmed = raw.Trim();

        if (trimmed.StartsWith("```") && trimmed.Contains('\n'))
        {
            var firstNewline = trimmed.IndexOf('\n');
            trimmed = trimmed[(firstNewline + 1)..];
            var closingFence = trimmed.LastIndexOf("```");
            if (closingFence >= 0)
            {
                trimmed = trimmed[..closingFence];
            }
        }

        var start = trimmed.IndexOf('{');
        var end = trimmed.LastIndexOf('}');
        if (start >= 0 && end > start)
        {
            return trimmed[start..(end + 1)];
        }

        return trimmed;
    }

    private static string SanitizeJson(string json)
    {
        if (string.IsNullOrWhiteSpace(json)) return json;

        // LLMs sometimes output literal newlines inside JSON strings which is invalid JSON.
        // This simple regex-based approach escapes them. 
        // We look for a newline that is NOT preceded by a backslash.
        // Actually, a simpler way for LLM output is to just replace \r\n and \n with \n string
        // but that might break correctly escaped ones.
        
        // Let's use a more robust search: replace literal newlines only if they appear 
        // between quotes of a key-value pair. But for now, since we expect NO literal newlines 
        // in a valid JSON structure except as separators, we can try to be smart.
        
        var sb = new StringBuilder();
        bool inString = false;
        for (int i = 0; i < json.Length; i++)
        {
            char c = json[i];
            if (c == '"' && (i == 0 || json[i - 1] != '\\'))
            {
                inString = !inString;
                sb.Append(c);
            }
            else if (inString && (c == '\n' || c == '\r'))
            {
                if (c == '\r' && i + 1 < json.Length && json[i + 1] == '\n') i++;
                sb.Append("\\n");
            }
            else
            {
                sb.Append(c);
            }
        }
        return sb.ToString();
    }

    private static IReadOnlyCollection<GeneratedIdeaDto> NormalizeIdeas(IReadOnlyCollection<RawIdea>? ideas, int count)
    {
        if (ideas is null || ideas.Count == 0)
        {
            return Array.Empty<GeneratedIdeaDto>();
        }

        var normalized = ideas
            .Where(i => !string.IsNullOrWhiteSpace(i.Title) && !string.IsNullOrWhiteSpace(i.Hook) && !string.IsNullOrWhiteSpace(i.Caption))
            .Take(count)
            .Select((idea, idx) => new GeneratedIdeaDto(
                Id: $"ai-{DateTimeOffset.UtcNow.ToUnixTimeMilliseconds()}-{idx + 1}",
                Type: NormalizeType(idea.Type),
                Hook: idea.Hook!.Trim(),
                Title: idea.Title!.Trim(),
                Caption: idea.Caption!.Trim(),
                Hashtags: NormalizeHashtags(idea.Hashtags),
                Platforms: NormalizePlatforms(idea.Platforms),
                BestTime: string.IsNullOrWhiteSpace(idea.BestTime) ? "19:00 - 21:00" : idea.BestTime.Trim(),
                EstimatedReach: string.IsNullOrWhiteSpace(idea.EstimatedReach) ? "5K - 20K" : idea.EstimatedReach.Trim()
            ))
            .ToList();

        return normalized;
    }

    private static string NormalizeType(string? type)
    {
        if (string.IsNullOrWhiteSpace(type)) return "Reel";
        var val = type.Trim().ToLowerInvariant();

        return val switch
        {
            "reel" => "Reel",
            "carousel" => "Carousel",
            "photo" => "Photo",
            "thread" => "Thread",
            "short" => "Short",
            _ => "Reel"
        };
    }

    private static IReadOnlyCollection<string> NormalizeHashtags(IReadOnlyCollection<string>? hashtags)
    {
        if (hashtags is null || hashtags.Count == 0)
        {
            return new[] { "#contentcreator", "#socialmedia", "#syncra" };
        }

        return hashtags
            .Select(h => h?.Trim() ?? string.Empty)
            .Where(h => !string.IsNullOrWhiteSpace(h))
            .Select(h => h.StartsWith('#') ? h : $"#{h}")
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .Take(8)
            .ToList();
    }

    private static IReadOnlyCollection<string> NormalizePlatforms(IReadOnlyCollection<string>? platforms)
    {
        if (platforms is null || platforms.Count == 0)
        {
            return new[] { "TikTok", "Instagram" };
        }

        return platforms
            .Select(p => p?.Trim())
            .Where(p => !string.IsNullOrWhiteSpace(p))
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .Take(4)
            .ToList()!;
    }

    private static IReadOnlyCollection<RepurposeAtomDto> NormalizeRepurposeAtoms(
        IReadOnlyCollection<RawRepurposeAtom>? atoms,
        IReadOnlyCollection<string> requestedPlatforms,
        bool extractAtoms)
    {
        if (atoms is null || atoms.Count == 0)
        {
            return Array.Empty<RepurposeAtomDto>();
        }

        var requested = requestedPlatforms
            .Select(NormalizeRepurposePlatform)
            .Where(p => !string.IsNullOrWhiteSpace(p))
            .ToHashSet(StringComparer.OrdinalIgnoreCase);

        var filtered = atoms
            .Where(a => !string.IsNullOrWhiteSpace(a.Content))
            .Select((a, idx) =>
            {
                var normalizedType = NormalizeRepurposeType(a.Type, extractAtoms);
                var platform = NormalizeRepurposePlatform(a.Platform);
                if (string.IsNullOrWhiteSpace(platform) || !requested.Contains(platform))
                {
                    platform = requested.FirstOrDefault() ?? "LinkedIn";
                }

                return new RepurposeAtomDto(
                    Id: $"rep-{DateTimeOffset.UtcNow.ToUnixTimeMilliseconds()}-{idx + 1}",
                    Type: normalizedType,
                    Title: string.IsNullOrWhiteSpace(a.Title) ? null : a.Title.Trim(),
                    Content: a.Content!.Trim(),
                    Platform: platform,
                    SuggestedHashtags: NormalizeHashtags(a.SuggestedHashtags).Take(6).ToList(),
                    SuggestedCta: string.IsNullOrWhiteSpace(a.SuggestedCta) ? null : a.SuggestedCta.Trim()
                );
            })
            .ToList();

        if (extractAtoms)
        {
            var atomTypes = new HashSet<string>(StringComparer.OrdinalIgnoreCase) { "INSIGHT", "TIP", "QUOTE" };
            filtered = filtered.Where(f => atomTypes.Contains(f.Type)).ToList();
        }

        return filtered.Take(20).ToList();
    }

    private static string NormalizeRepurposePlatform(string? platform)
    {
        if (string.IsNullOrWhiteSpace(platform)) return string.Empty;
        var val = platform.Trim().ToLowerInvariant();

        return val switch
        {
            "linkedin" => "LinkedIn",
            "x" => "X",
            "twitter" => "X",
            "instagram" => "Instagram",
            "newsletter" => "Newsletter",
            _ => AllowedRepurposePlatforms.FirstOrDefault(p => p.Equals(platform.Trim(), StringComparison.OrdinalIgnoreCase)) ?? string.Empty
        };
    }

    private static string NormalizeRepurposeType(string? type, bool extractAtoms)
    {
        if (string.IsNullOrWhiteSpace(type))
        {
            return extractAtoms ? "INSIGHT" : "POST";
        }

        var upper = type.Trim().ToUpperInvariant();
        if (AllowedRepurposeTypes.Contains(upper, StringComparer.OrdinalIgnoreCase))
        {
            return upper;
        }

        return extractAtoms ? "INSIGHT" : "POST";
    }

